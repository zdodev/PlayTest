struct Calculator {
    func add(_ lhs: Int, _ rhs: Int) -> Int {
        lhs + rhs
    }
    
    func subtract(_ lhs: Int, _ rhs: Int) -> Int {
        lhs - rhs
    }
    
    func multiply(_ lhs: Int, _ rhs: Int) -> Int {
        lhs * rhs
    }
    
    func divide(_ lhs: Int, _ rhs: Int) -> Int {
        lhs / rhs
    }
}
